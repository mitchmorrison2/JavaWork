public class GreedyBanker extends Banker {
	
	public GreedyBanker() {
		super();
	}

	public int calculateOffer(int total, int caseCount) {
		int s = caseCount;
		int endTotal = total;
		
		//int selectedCase = selectCase()
		return offer = (endTotal / s) * 2;
	}
	
}
